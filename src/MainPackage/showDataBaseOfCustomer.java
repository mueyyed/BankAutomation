    public void showDataInlist()
    {
        refreshDataInFrame();
        DefaultListModel  model = new DefaultListModel();
         
        jList1.setModel(model);
        String ch="  "; 
     
        for(int i=0; i<Database.CutomerData.size(); i++)
        {
            model.addElement(Database.CutomerData.elementAt(i).Ad+ ch +Database.CutomerData.elementAt(i).Soyad
                    +ch +Database.CutomerData.elementAt(i).Eposta
                    +ch+Database.CutomerData.elementAt(i).Sifre+ch+Database.CutomerData.elementAt(i).bakiye);
        }
    }
         
    public void refreshDataInFrame()
    {
         DefaultListModel  model = new DefaultListModel();
        jList1.setModel(model);
        model.clear(); 
    }
    
    
    
